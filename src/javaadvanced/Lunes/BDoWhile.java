package javaadvanced.Lunes;

/**
 * Hacer mientras
 * do {
 * } while(condition) 
 */
public class BDoWhile {
    public static void main(String[] args) 
        {
            int i = 1;
            
            do{
                System.out.println(i);
                i++;
            }while(i<=10);
        }
}
